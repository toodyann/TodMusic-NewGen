import { useState, useRef, useEffect } from "react";
import Logo from "./Logo.jsx";
import Input from "./Input.jsx";
import ThemeToggle from "./ThemeToggle.jsx";
import "../styles/scss/Header/header.scss";

export default function Header({
  searchValue,
  onSearch,
  onSearchChange,
  onHomeClick,
  onMenuClick,
  onProfileClick,
  onSettingsClick,
  activeTab,
  isMobile,
  selectedPlaylist,
  t,
}) {
  const [isMenuOpen, setIsMenuOpen] = useState(false);
  const menuRef = useRef(null);

  useEffect(() => {
    const handleClickOutside = (event) => {
      if (menuRef.current && !menuRef.current.contains(event.target)) {
        setIsMenuOpen(false);
      }
    };

    if (isMenuOpen) {
      document.addEventListener("mousedown", handleClickOutside);
    }

    return () => {
      document.removeEventListener("mousedown", handleClickOutside);
    };
  }, [isMenuOpen]);

  const handleProfileMenuClick = () => {
    setIsMenuOpen(false);
    onProfileClick();
  };

  const handleSettingsMenuClick = () => {
    setIsMenuOpen(false);
    onSettingsClick();
  };

  const showSearch =
    !isMobile ||
    (activeTab !== "playlists" &&
      activeTab !== "profile" &&
      activeTab !== "settings" &&
      !selectedPlaylist &&
      (activeTab === "home" || activeTab === "search"));

  return (
    <header className="app-header">
      <button className="burger-btn" onClick={onMenuClick}>
        <svg
          xmlns="http://www.w3.org/2000/svg"
          width="24"
          height="24"
          viewBox="0 0 24 24"
          fill="none"
          stroke="currentColor"
          strokeWidth="2"
          strokeLinecap="round"
          strokeLinejoin="round"
        >
          <line x1="3" y1="12" x2="21" y2="12"></line>
          <line x1="3" y1="6" x2="21" y2="6"></line>
          <line x1="3" y1="18" x2="21" y2="18"></line>
        </svg>
      </button>
      <Logo />
      {showSearch && (
        <Input
          placeholder={t ? t("searchPlaceholder") : "Пошук музики..."}
          value={searchValue}
          onChange={(e) => onSearchChange(e.target.value)}
          onSearch={onSearch}
          onHomeClick={onHomeClick}
          t={t}
        />
      )}
      <div className="profile-menu-container" ref={menuRef}>
        <button
          className={`profile-btn ${isMenuOpen ? "active" : ""}`}
          onClick={() => setIsMenuOpen(!isMenuOpen)}
        >
          <svg
            xmlns="http://www.w3.org/2000/svg"
            width="24"
            height="24"
            viewBox="0 0 24 24"
            fill="none"
            stroke="currentColor"
            strokeWidth="2"
            strokeLinecap="round"
            strokeLinejoin="round"
          >
            <line x1="3" y1="12" x2="21" y2="12"></line>
            <line x1="3" y1="6" x2="21" y2="6"></line>
            <line x1="3" y1="18" x2="21" y2="18"></line>
          </svg>
        </button>
        {isMenuOpen && (
          <div className="profile-dropdown">
            <button className="dropdown-item" onClick={handleProfileMenuClick}>
              <svg
                xmlns="http://www.w3.org/2000/svg"
                width="20"
                height="20"
                viewBox="0 0 24 24"
                fill="none"
                stroke="currentColor"
                strokeWidth="2"
                strokeLinecap="round"
                strokeLinejoin="round"
              >
                <path d="M20 21v-2a4 4 0 0 0-4-4H8a4 4 0 0 0-4 4v2"></path>
                <circle cx="12" cy="7" r="4"></circle>
              </svg>
              <span>{t ? t("profile") : "Профіль"}</span>
            </button>
            <div className="dropdown-divider"></div>
            <button className="dropdown-item" onClick={handleSettingsMenuClick}>
              <svg
                xmlns="http://www.w3.org/2000/svg"
                width="20"
                height="20"
                viewBox="0 0 24 24"
                fill="none"
                stroke="currentColor"
                strokeWidth="2"
                strokeLinecap="round"
                strokeLinejoin="round"
              >
                <path d="M12.22 2h-.44a2 2 0 0 0-2 2v.18a2 2 0 0 1-1 1.73l-.43.25a2 2 0 0 1-2 0l-.15-.08a2 2 0 0 0-2.73.73l-.22.38a2 2 0 0 0 .73 2.73l.15.1a2 2 0 0 1 1 1.72v.51a2 2 0 0 1-1 1.74l-.15.09a2 2 0 0 0-.73 2.73l.22.38a2 2 0 0 0 2.73.73l.15-.08a2 2 0 0 1 2 0l.43.25a2 2 0 0 1 1 1.73V20a2 2 0 0 0 2 2h.44a2 2 0 0 0 2-2v-.18a2 2 0 0 1 1-1.73l.43-.25a2 2 0 0 1 2 0l.15.08a2 2 0 0 0 2.73-.73l.22-.39a2 2 0 0 0-.73-2.73l-.15-.08a2 2 0 0 1-1-1.74v-.5a2 2 0 0 1 1-1.74l.15-.09a2 2 0 0 0 .73-2.73l-.22-.38a2 2 0 0 0-2.73-.73l-.15.08a2 2 0 0 1-2 0l-.43-.25a2 2 0 0 1-1-1.73V4a2 2 0 0 0-2-2z"></path>
                <circle cx="12" cy="12" r="3"></circle>
              </svg>
              <span>{t ? t("settings") : "Налаштування"}</span>
            </button>
            <div className="dropdown-divider"></div>
            <button className="dropdown-item">
              <svg
                xmlns="http://www.w3.org/2000/svg"
                width="20"
                height="20"
                viewBox="0 0 24 24"
                fill="none"
                stroke="currentColor"
                strokeWidth="2"
                strokeLinecap="round"
                strokeLinejoin="round"
              >
                <circle cx="12" cy="12" r="10"></circle>
                <path d="M12 16v-4"></path>
                <path d="M12 8h.01"></path>
              </svg>
              <span>{t ? t("about") : "Про додаток"}</span>
            </button>
          </div>
        )}
      </div>
    </header>
  );
}
